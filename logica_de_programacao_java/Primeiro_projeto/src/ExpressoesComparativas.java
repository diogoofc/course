public class ExpressoesComparativas {
    public static void main(String[] args) {

       int chocolate  = 10;
       int refrigerante = 20;

       System.out.println(chocolate == refrigerante);
       System.out.println(chocolate != refrigerante);
       System.out.println(chocolate > refrigerante);
       System.out.println(chocolate < refrigerante);
       System.out.println(chocolate >= refrigerante);
       System.out.println(chocolate <= refrigerante);

    }

}